export function gt(x, y) {
    return x > y;
}

export function ge(x, y) {
    return x >= y;
}

export function lt(x, y) {
    return x < y;
}

export function le(x, y) {
    return x <= y;
}

export function eq(x, y) {
    return x == y;
}

export function ne(x, y) {
    return x != y;
}

export function has(x, y) {
    return (x & y) == y;
}